# Find out how many numbers are square numbers, cube numbers, and how many are 5th power numbers below 1000.
print(f"Square numbers count below 1000 is {int(1000**(1/2))}")
print(f"Cube numbers count below 1000 is {int(1000**(1/3))}")
print(f"5th power numbers count below 1000 is {int(1000**(1/5))}")